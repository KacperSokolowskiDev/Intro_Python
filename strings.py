phrase = "Python Lessons"
print(phrase + " are cool")  # concatenation
print(phrase.lower())  # lowercase
print(phrase.upper())  # uppercase
print(phrase.upper().isupper())  # boolean true if it's uppercase
print(len(phrase))  # length of the string
print(phrase[3])  # printing the 4th letter
print(phrase.index("o"))  # looking for the index of a element
print(phrase.replace("Python", "Javascript"))  # replacing a value
